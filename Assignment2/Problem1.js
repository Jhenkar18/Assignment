//Declare different types of variables and print the type of each variable?

var numVar = 10;
console.log(typeof numVar); // Output: number        
var strVar = "Haii";
console.log(typeof strVar); // Output: string   
var boolVar = true;
console.log(typeof boolVar); // Output: boolean     

var objVar = {          
    name: "Tom",
    age: 25
};
console.log(typeof objVar); // Output: object
var funcVar = function() {   
    console.log("Function");
};
console.log(typeof funcVar); // Output: function






